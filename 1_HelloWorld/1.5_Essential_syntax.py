# Statements

# Statements are usually single-line commands
print("abc")

# Python is capable of multi-line statements
print("abc",
      "def")

# It's also possible to execute multiple statements in one line
a=1; b=2; c=3

print(a, b, c)

# Alternative
a, b, c = 4, 5, 6
# This is more common

print(a, b, c)